d_month = {1: "Январь", 2: "Февраль", 3: "Март", 4: "Апрель", 5: "Май", 6: "Июнь",
           7: "Июль", 8: "Август", 9: "Сентябрь", 10: "Октябрь", 11: "Ноябрь", 12: "Декабрь"}
d = d_month

while True:
    month = int(input("Введите порядковый номер месяца: "))
    m = month
    if m in d:
        print(f"{m} - {d[m]}")
        break
    else:
        print("Нужно ввести число от 1 до 12")
